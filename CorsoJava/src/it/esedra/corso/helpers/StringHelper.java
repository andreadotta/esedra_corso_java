package it.esedra.corso.helpers;

public class StringHelper {

	
	/**
	 * Concatena due stringhe con uno spazio nel mezzo
	 * @param a stringa input
	 * @param b String input 
	 * @return String concatenata
	 */
	public static String stringConcatWithSpace(String a, String b) {
		return a + " " + b;
	}
}
