package it.esedra.corso.oussein.JUnit;

public class OperaiDetail {
	private String nome;
	private Double salaryMensile;
	private int age;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Double getSalaryMensile() {
		return salaryMensile;
	}
	public void setSalaryMensile(Double salaryMensile) {
		this.salaryMensile = salaryMensile;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}

}

